    <p>The early Gupta drachmas were derived from the earlier coins of Kshatrapas (obverse was practically unchanged,
        while the mountain on reverse was replaced with the image of Garuda).

        Kumaragupta I (Mahendraditya) was a ruler of the Gupta Empire in 415455 CE. Like his father and predecessor,
        Chandragupta II, Kumaragupta was an able ruler. He retained, intact, the vast empire, which extended from Bengal
        to Kathiawar and from the Himalayas to the Narmada. He issued silver coins in the style of the newly conquered
        Western Kshatrapas, though the Kshatrapa symbols on the reverse were replaced with the Gupta symbol Garuda.</p>

